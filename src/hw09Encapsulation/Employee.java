package hw09Encapsulation;

public class Employee {
	
	private String employeeName;
	private int employeeAge;
	private char employeeGender;
	private boolean employeeCitizen;
	
	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public int getEmployeeAge() {
		return employeeAge;
	}

	public void setEmployeeAge(int employeeAge) {
		this.employeeAge = employeeAge;
	}

	public char getEmployeeGender() {
		return employeeGender;
	}

	public void setEmployeeGender(char employeeGender) {
		this.employeeGender = employeeGender;
	}

	public boolean getEmployeeCitizen() {
		return employeeCitizen;
	}

	public void setEmployeeCitizen(boolean employeeCitizen) {
		this.employeeCitizen = employeeCitizen;
	}


	

}
